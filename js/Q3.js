let currency = 'USD';

switch (currency) {

    case 'INR': console.log("Indian Rupee")
        break

    case 'EUR': console.log("Euro")
        break

    case 'USD': console.log("US Dollar")
        break

    case 'LKR': console.log(" Sri Lanka Rupee")
        break

    case 'MXN': console.log("Mexican Peso")
        break

    default: console.log("Unknown Currency")
       
}